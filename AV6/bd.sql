CREATE OR REPLACE TYPE TP_CONTRATO AS OBJECT(
    numero_contrato INTEGER,
    tipo_contrato VARCHAR2(80),
    data_admissao DATE, 
    cargo VARCHAR2(50), 
    salario_bruto_mensal NUMBER(10,2),
    jornada_trabalho_semanal INTEGER,

    ORDER MEMBER FUNCTION comparaSalario (f TP_CONTRATO) RETURN INTEGER,

    CONSTRUCTOR FUNCTION TP_CONTRATO(numero_contrato INTEGER, tipo_contrato VARCHAR2, data_admissao DATE, 
        cargo VARCHAR2, salario_bruto_mensal NUMBER, jornada_trabalho_semanal INTEGER)
        RETURN SELF AS RESULT
)NOT FINAL;
/

CREATE OR REPLACE TYPE BODY TP_CONTRATO AS
    CONSTRUCTOR FUNCTION TP_CONTRATO(numero_contrato INTEGER, tipo_contrato VARCHAR2
        data_admissao DATE, cargo VARCHAR2, salario_bruto_mensal NUMBER, jornada_trabalho_semanal INTEGER)
        RETURN SELF AS RESULT AS 
            BEGIN 
                SELF.num_contrato := num_contrato
                SELF.tipo_contrato := tipo_contrato
                SELF.data_admissao := data_admissao
                SELF.cargo := cargo
                SELF.salario_bruto_mensal := salario_bruto_mensal
                SELF.jornada_trabalho_semanal := jornada_trabalho_semanal 
                RETURN;
            END;
    
    ORDER MEMBER FUNCTION comparaSalario(c TP_CONTRATO) RETURN INTEGER IS
    BEGIN
        RETURN SELF.salario_bruto_mensal - c.salario_bruto_mensal;
    END;

END;
/

