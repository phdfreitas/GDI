CREATE OR REPLACE TYPE BODY TP_FUNCIONARIO AS
    MEMBER FUNCTION salarioBrutoAnual RETURN NUMBER IS
    BEGIN
        RETURN numero_contrato_func.salario_bruto_mensal * 12
    END;
END;
